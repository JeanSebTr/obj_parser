#ifndef _COORD_H
#define	_COORD_H

struct dVect3
{
    double x;
    double y;
    double z;
};

struct dVect2
{
    double x;
    double y;
};

struct uiVect3
{
    unsigned int x;
    unsigned int y;
    unsigned int z;
};

struct uiVect2
{
    unsigned int x;
    unsigned int y;
};

struct Angle3
{
    short int x;
    short int y;
};

struct dCoord3
{
    dVect3 pos;
    Angle3 a;
};

struct dCoord2
{
    dVect2 pos;
    double a;
};

struct uiCoord3
{
    uiVect3 pos;
    Angle3 a;
};

struct uiCoord2
{
    uiVect2 pos;
    short int a;
};

#endif	/* _COORD_H */

